package Practice;

public class SwapTwoNumbers {

	public static void main(String[] args) {
		
		
		int a = 5;
		int b = 10;
		
		System.out.println("A is: "+a);
		System.out.println("B is: "+b);

		
		
		int temp;
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("A is "+a);
		System.out.println("B is "+
		b);
	}

}
